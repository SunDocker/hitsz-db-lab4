package cn.edu.hitsz.entity;

/**
 * @author SunDocker
 */
public class CheckShow {
    public String name;
    public String volunteerAccount;
    public String checkStatus;
    public String signupTime;
    public String no;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getVolunteerAccount() {
        return volunteerAccount;
    }

    public void setVolunteerAccount(String volunteerAccount) {
        this.volunteerAccount = volunteerAccount;
    }

    public String getCheckStatus() {
        return checkStatus;
    }

    public void setCheckStatus(String checkStatus) {
        this.checkStatus = checkStatus;
    }

    public String getSignupTime() {
        return signupTime;
    }

    public void setSignupTime(String signupTime) {
        this.signupTime = signupTime;
    }

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }
}
